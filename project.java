    public void upDateDB()
    {
        try 
        {
            Class.forName("com.mysql.jdbc.Driver");
            p = DriverManager.getConnection(HomeInterior,username,password);
            pst = p.prepareStatement("select * from project");
            
            rs = pst.executeQuery();
            ResultSetMetaData dData = rs.getMetaData();
            
            q = dData.getColumnCount();
            
            DefaultTableModel RecordTable = (DefaultTableModel)jTable1.getModel();
            RecordTable.setRowCount(0);
            
            while(rs.next()) {
                Vector columnData = new Vector();
                
                for(i = 1;i <= q; i++)
                {
                    columnData.add(rs.getString("id"));
                    columnData.add(rs.getString("PROJECT_ID"));
                    columnData.add(rs.getString("CLIENT_ID"));
                    columnData.add(rs.getString("SUPPLIER_ID"));
                    columnData.add(rs.getString("PROJECT_NAME"));
                    columnData.add(rs.getString("START_DATE"));
                    columnData.add(rs.getString("END_DATE"));
                }
                RecordTable.addRow(columnData);
            }
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
