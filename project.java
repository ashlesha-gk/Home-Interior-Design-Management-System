    private void jbtnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnInsertActionPerformed
    try {
        Class.forName("com.mysql.jdbc.Driver");
        p = DriverManager.getConnection(HomeInterior, username, password);
        
        // Calculate project status based on start and end dates
        String projectStatus;
        LocalDate startDate = LocalDate.parse(jtxtSDate.getText());
        LocalDate endDate = LocalDate.parse(jtxtEDate.getText());
        LocalDate currentDate = LocalDate.now();
        
        if (startDate.isAfter(currentDate)) {
            projectStatus = "Scheduled";
        } else if (startDate.isBefore(currentDate) && endDate.isAfter(currentDate)) {
            projectStatus = "In Progress";
        } else {
            projectStatus = "Completed";
        }
        
        // Prepare and execute the insertion query
        pst = p.prepareStatement("insert into PROJECT(PROJECT_ID, CLIENT_ID, SUPPLIER_ID, PROJECT_NAME, START_DATE, END_DATE, PROJECT_STATUS) values (?, ?, ?, ?, ?, ?, ?)");
        pst.setString(1, jtxtPID.getText());
        pst.setString(2, jtxtCID.getText());
        pst.setString(3, jtxtSID.getText());
        pst.setString(4, jtxtName.getText());
        pst.setString(5, jtxtSDate.getText());
        pst.setString(6, jtxtEDate.getText());
        pst.setString(7, projectStatus);
        
        pst.executeUpdate();
        
        JOptionPane.showMessageDialog(this, "Record Added");
        upDateDB();
    } catch (ClassNotFoundException ex) {
        java.util.logging.Logger.getLogger(project.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
    } catch (SQLException ex) {
        java.util.logging.Logger.getLogger(project.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
    }

    }//GEN-LAST:event_jbtnInsertActionPerformed

